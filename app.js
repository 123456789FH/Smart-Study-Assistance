function pageLink(page) { return `assets/guide.pdf#page=${page}`; }

function populateEducationTypes() {
  educationType.innerHTML = Object.entries(catalogs).map(([key, value]) => `<option value="${key}">${value.label}</option>`).join('');
  educationType.value = state.typeKey;
  populateStages();
}

function populateStages() {
  const stages = catalogs[educationType.value].stages;
  stageSelect.innerHTML = Object.entries(stages).map(([key, value]) => `<option value="${key}">${value.label}</option>`).join('');
  if (!stages[state.stageKey]) state.stageKey = Object.keys(stages)[0];
  stageSelect.value = state.stageKey;
  populateGrades();
}

function populateGrades() {
  const grades = catalogs[educationType.value].stages[stageSelect.value].grades;
  gradeSelect.innerHTML = Object.entries(grades).map(([key, value]) => `<option value="${key}">${value.label}</option>`).join('');
  if (!grades[state.gradeKey]) state.gradeKey = Object.keys(grades)[0];
  gradeSelect.value = state.gradeKey;
}

function getCurrentPlan() {
  const type = catalogs[educationType.value];
  const stage = type.stages[stageSelect.value];
  const plan = stage.grades[gradeSelect.value];
  return { type, stage, plan, typeKey: educationType.value, stageKey: stageSelect.value, gradeKey: gradeSelect.value };
}

function getPlanKind(plan) { return plan.kind || 'hours'; }
function getPlanPages(plan) {
  const pages = [plan.page];
  if (plan.extraPage && plan.extraPage !== plan.page) {
    for (let p = plan.page + 1; p <= plan.extraPage; p += 1) pages.push(p);
  }
  return pages;
}

